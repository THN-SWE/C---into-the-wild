// this is a comment in C
//  std-standard io- input/
#include <stdio.h>

int main()
{
    /*
    \n - newline escape sequence
    \t - tab escape sequence
    */
    printf("hello world\n");
    printf("I run C\n");

    printf("1\t2\t3\t\n");
    
    // creating a grid using tab esc seq
    printf("\n1\t2\t3\t\n");
    printf("4\t5\t6\t\n");
    printf("7\t8\t9\t\n");

    // using quotes
    printf("\"I'm a C programmer\"\n");
    return 0;
}
